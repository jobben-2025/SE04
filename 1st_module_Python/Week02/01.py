# Python Quick Demo: What can Python do?

# A list of things Python can do
python_powers = [
    "Web Development 🌐",
    "Data Science 📊",
    "Artificial Intelligence 🤖",
    "Automation ⚙️",
    "Game Development 🎮"
]

# Print a welcome message
print("🚀 Welcome to Python! Here's what Python can do:")

# Use a loop to display each item from the list
for power in python_powers:
    print(f"• {power}")

# A fun calculation example
print("\n✨ Let's do some math:")
result = sum(range(1, 11))  # Sum of numbers from 1 to 10
print(f"The sum of numbers from 1 to 10 is: {result}")
